package in.co.rays.basics;

public class ElseIf {
	public static void main(str[] args) {
		int percent = 51;

		if (percent >= 90) {
			System.out.println("your grade is A+");
		} else if (percent >= 80) {
			System.out.println("your grade is A");
		} else if (percent >= 70) {
			System.out.println("your grade is B");
		} else if (percent >= 60) {
			System.out.println("your grade is B+");
		} else if (percent >= 50) {
			System.out.println("your grade is C");
		} else {
			System.out.println("you are fail");
		}
	}

}
